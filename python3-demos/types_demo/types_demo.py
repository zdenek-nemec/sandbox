def main():
    integer_number = 7
    print(integer_number, type(integer_number))

    float_number = 7.0
    print(float_number, type(float_number))

    complex_number = 7 + 4j
    print(complex_number, type(complex_number))

    boolean = True
    print(boolean, type(boolean))


if __name__ == "__main__":
    main()
