public class DemoModulo {
    public static void main(String[] args) {
        System.out.println("Demo: Modulo");

        System.out.println("5 % 3 = " + 5 % 3);
        System.out.println("-5 % 3 = " + -5 % 3);
        System.out.println("5 % -3 = " + 5 % -3);
        System.out.println("-5 % -3 = " + -5 % -3);
    }
}
